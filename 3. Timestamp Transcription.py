# 2ï¸âƒ£  Imports
import os
import glob
import stable_whisper as whisper
from tqdm import tqdm
import pandas as pd
from datetime import timedelta
import subprocess

# 3ï¸âƒ£  Automatically find a .wav file in /content
wav_files = [f for f in os.listdir(".") if f.lower().endswith(".wav")]
if not wav_files:
    raise RuntimeError("âŒ  No '.wav' files found in the working directory. "
                       "Please upload a .wav file (e.g. via files.upload()) and re-run.")
AUDIO_FILE = wav_files[0]
print(f"ğŸ”â€Š Using AUDIO_FILE = '{AUDIO_FILE}'")

# 4ï¸âƒ£  Check directory contents before splitting
print("\nğŸ“‚  Files in /content before splitting:")
for f in os.listdir("."):
    print("   ", f)

# 5ï¸âƒ£  Prepare a folder for ~60 s chunks
CHUNK_DIR = "audio_chunks"
os.makedirs(CHUNK_DIR, exist_ok=True)

# 6ï¸âƒ£  Use ffmpeg to slice the .wav into ~60 s pieces (show ffmpeg output)
segment_time = 60  # seconds per chunk (adjust if needed)
split_cmd = [
    "ffmpeg",
    "-i", AUDIO_FILE,
    "-f", "segment",
    "-segment_time", str(segment_time),
    "-c", "copy",
    os.path.join(CHUNK_DIR, "chunk_%04d.wav")
]
print(f"\nğŸ”Š  Splitting '{AUDIO_FILE}' into ~{segment_time}s chunksâ€¦")
ffmpeg_proc = subprocess.run(split_cmd)  # ffmpeg stdout/stderr will appear here
if ffmpeg_proc.returncode != 0:
    raise RuntimeError(f"âŒ  ffmpeg exited with code {ffmpeg_proc.returncode}. "
                       "Check the ffmpeg messages above for the cause.")

# 7ï¸âƒ£  Verify that chunks exist
print("\nğŸ“‚  Files in 'audio_chunks/' after splitting:")
chunk_files = sorted(glob.glob(os.path.join(CHUNK_DIR, "chunk_*.wav")))
for cf in chunk_files:
    print("   ", cf)
print(f"â„¹ï¸  Found {len(chunk_files)} chunk file(s).")
if len(chunk_files) == 0:
    raise RuntimeError("âŒ  No chunk files were created! Please check that ffmpeg could read AUDIO_FILE.")

# 8ï¸âƒ£  Load a smaller Whisper model (use 'small' or 'tiny' to save RAM)
model = whisper.load_model("small")  # ~1 GB RAM; switch to "tiny" (~300 MB) if needed

# 9ï¸âƒ£  Prepare list to hold sentence-level timing dicts
sentence_rows = []

for idx, chunk_path in enumerate(tqdm(chunk_files, desc="Processing chunks")):
    # â€“ Each chunkâ€™s offset in the original file (in seconds)
    chunk_offset = idx * segment_time

    # â€“ Transcribe this chunk with ts_num=16 (âˆ¼1 ms precision)
    result = model.transcribe(
        chunk_path,
        word_timestamps=True,
        ts_num=16
    )

    # â€“ Gather all words from every segment into one list
    all_words_in_chunk = []
    for seg in result.segments:
        if hasattr(seg, "words") and seg.words:
            all_words_in_chunk.extend(seg.words)

    # â€“ Build list of â€œsentencesâ€ (Whisper segments)
    all_sentences_in_chunk = [seg.text.strip() for seg in result.segments]

    # â€“ For each sentence, use the corresponding wordâ€slice to get start/end
    w_idx = 0
    for s in all_sentences_in_chunk:
        tokens = s.split()
        token_count = len(tokens)
        if token_count == 0:
            continue

        word_slice = all_words_in_chunk[w_idx : w_idx + token_count]
        if not word_slice:
            # If not enough wordâ€tokens exist, skip alignment for this sentence
            w_idx += token_count
            continue

        raw_start = word_slice[0].start
        raw_end   = word_slice[-1].end

        s_start = raw_start + chunk_offset
        s_end   = raw_end   + chunk_offset

        sentence_rows.append({
            "sentence":   s,
            "start_s":    s_start,
            "end_s":      s_end,
            "duration_s": s_end - s_start,
            "start_time": str(timedelta(seconds=s_start)),
            "end_time":   str(timedelta(seconds=s_end))
        })
        w_idx += token_count

# ğŸ”Ÿ  Convert the sentence list into a DataFrame and write to sentence_timestamps.csv
df_sentences = pd.DataFrame(sentence_rows)

# Ensure the first column is named "sentence"
df_sentences = df_sentences[["sentence", "start_s", "end_s", "duration_s", "start_time", "end_time"]]

SENT_CSV = "sentence_timestamps.csv"
df_sentences.to_csv(SENT_CSV, index=False, float_format="%.6f")
print(f"\nâœ…  Saved sentence-level timings â†’ '{SENT_CSV}' ({len(df_sentences)} rows).")

# â”€â”€â”€ Cell 8: Save & Preview â”€â”€â”€
